package com.hh.config.domain;

/**
 * init.xml数据的实体类
 * @author hh
 * 2017-10-10 15:45:51
 */
public class Init {
	private String name;
	private String classpath;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getClasspath() {
		return classpath;
	}
	public void setClasspath(String classpath) {
		this.classpath = classpath;
	}
}
